def stock_data(names, start_time):

    df_rets = None
    df_rets_d = None
    df_rets_w = None

    for name in names:
        df = data.DataReader(str(name), 'yahoo', start_time)
        df_ret = df['Close'].pct_change()
        df_ret.index = pd.to_datetime(df_ret.index, format="%Y%m%d")

        if df_rets is None: 
            df_rets_d = pd.DataFrame(index = df_ret.index)
            df_rets_d[str(name)] = df_ret            
        else:
            df_rets_d[str(name)] = df_ret

        df_m = df_ret.resample('M').apply(compound).to_period('M')
        df_w = df_ret.resample('W').apply(compound).to_period('W')
        
        if df_rets is None: 
            df_rets = pd.DataFrame(index = df_m.index)
            df_rets[str(name)] = df_m
            df_rets_w = pd.DataFrame(index = df_w.index)
            df_rets_w[str(name)] = df_w
            
        else:
            df_rets[str(name)] = df_m
            df_rets_w[str(name)] = df_w


    return df_rets, df_rets_d, df_rets_w


